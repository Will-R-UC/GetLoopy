public class Loops {
    public static void main(String[] args) {
        //Task 1
        System.out.println("# Task 1");
        for (int i = 0; i <= 30; i++) {
            System.out.print(i + " ");
        }

        System.out.print("\n\n");

        //Task 2
        System.out.println("# Task 2");
        for (int i = 30; i >= 0; i--) {
            System.out.print(i + " ");
        }

        System.out.print("\n\n");

        //Task 3
        System.out.println("# Task 3");
        for (int i = 0; i <= 18; i += 3) {
            System.out.print(i + " ");
        }

        System.out.print("\n\n");

        //Task 4
        System.out.println("# Task 4");
        for (int i = 10; i >= 0; i -= 2) {
            System.out.print(i + " ");
        }
    }
}
